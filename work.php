    <script>
        const buttons = document.querySelectorAll('#buttons-box button');
        const workContents = document.querySelectorAll('.work-content');

        function showElementsByClass(className) {
            workContents.forEach((element) => {
                element.style.display = 'none';
            });

            buttons.forEach((button) => {
                button.querySelector('span').classList.remove('active');
                button.classList.remove('active');
            });

            if (className === 'ALL') {
                workContents.forEach((element) => {
                    element.style.display = 'block';
                });
            } else {
                const selectedElements = document.querySelectorAll('.' + className);
                selectedElements.forEach((element) => {
                    element.style.display = 'block';
                });
            }

            const activeButton = document.getElementById(className);
            activeButton.querySelector('span').classList.add('active');
            activeButton.classList.add('active');
        }

        buttons.forEach((button) => {
            button.addEventListener('click', () => {
                showElementsByClass(button.id);
            });
        });

        showElementsByClass('ALL');

        document.addEventListener("DOMContentLoaded", function() {
            const workContents = document.querySelectorAll('.work-content');

            workContents.forEach((element, index) => {
                setTimeout(() => {
                    element.classList.add('fade-in');
                }, index * 100);
            });
        });

        document.addEventListener("DOMContentLoaded", function() {
            const buttonsBox = document.getElementById('buttons-box');
            buttonsBox.classList.add('slide-in-from-left');
        });
    </script>
